// Validate maps at load time so a typo fails loudly.
for (let i = 0; i < LEVELS.length; i++) {
  const m = LEVELS[i];
  if (m.length !== GRID_H) throw new Error(`level ${i + 1}: ${m.length} rows, want ${GRID_H}`);
  for (let r = 0; r < m.length; r++) {
    if (m[r].length !== GRID_W) throw new Error(`level ${i + 1} row ${r}: ${m[r].length} cols, want ${GRID_W}`);
  }
  if (!m.some((r) => r.includes('P'))) throw new Error(`level ${i + 1}: no P start`);
}

const ENEMY_CODES = { w: 'walker', j: 'jumper', f: 'floater', s: 'spitter' };

export function parseLevel(idx) {
  const map = LEVELS[idx];
  const g = { w: GRID_W, h: GRID_H, d: new Uint8Array(GRID_W * GRID_H) };
  const spawns = [];
  let p1 = { x: 24, y: 180 }, p2 = { x: 216, y: 180 };
  for (let ty = 0; ty < GRID_H; ty++) {
    for (let tx = 0; tx < GRID_W; tx++) {
      const ch = map[ty][tx];
      if (ch === '#') g.d[ty * GRID_W + tx] = T.SOLID;
      else if (ch === 'P') p1 = { x: tx * TILE + 1, y: ty * TILE + 2 };
      else if (ch === 'Q') p2 = { x: tx * TILE + 1, y: ty * TILE + 2 };
      else if (ENEMY_CODES[ch]) spawns.push({ t: ENEMY_CODES[ch], x: tx * TILE + 1, y: ty * TILE + 2 });
    }
  }
  return { grid: g, spawns, p1, p2, theme: Math.floor(idx / 5) % 4 };
}

export function tileAt(g, tx, ty) {
  if (tx < 0 || tx >= g.w) return T.SOLID; // side walls beyond the map
  ty = ((ty % g.h) + g.h) % g.h;           // vertical wrap
  return g.d[ty * g.w + tx];
}

export function solidAtPx(g, px, py) {
  return tileAt(g, Math.floor(px / TILE), Math.floor(py / TILE)) === T.SOLID;
}
